def upscale_mission_layout(mission_area_np_array, scale_x, scale_y):
    return np.kron(mission_area_np_array, np.ones((scale_y, scale_x)))


def resize_mission_layout(grid_data, target_width, target_height):
    original_height, original_width = grid_data.shape

    # Compute zoom factors
    zoom_y = target_height / original_height
    zoom_x = target_width / original_width

    # Resize using nearest-neighbor to preserve binary values
    resized = zoom(grid_data, (zoom_y, zoom_x), order=0)

    # Ensure it's still 0s and 1s
    return resized.astype(np.uint8)
